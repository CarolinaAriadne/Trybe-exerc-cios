# A divmod()função retorna uma tupla contendo o quociente e o resto quando argumento1 (dividendo) é dividido por argumento2 (divisor).

x = divmod(5, 2)

print(x)

#resultdo : (2,1)

# exemplificando: quantos dois cabem em 5? cabem 2 (quoficiente, primeiro argumento)
# qual o resto da divisão de 5/2? é 1, logo o segundo argumento do resultado é 1

